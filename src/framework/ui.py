def getChatBubble(message, is_user):
    return dbc.Row(
        dbc.Card(
            dbc.CardBody(
                message,
                className="w-auto"
            ),
            color="info" if is_user else "success",
            outline=True,
            className="chat-card user-card" if is_user else "chat-card bot-card"
        ),
        justify="end" if is_user else "start"
    )


